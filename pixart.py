import turtle

#a function for each requested color

def draw_black_pixel(size=40):
    turtle.down()
    angle=90
    turtle.fillcolor("black")
    turtle.begin_fill()
    for i in range(4):
        turtle.forward(size)
        turtle.right(angle)

    turtle.end_fill()
    turtle.up()
    turtle.forward(size)
